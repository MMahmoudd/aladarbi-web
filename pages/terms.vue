            <h2 class="sub2">٨-٤-١</h2>
            <p>يقر الأطراف أنه في حال إلغاء الطلب، فإنهم سيوجهون إشعاراً بشأن مثل هذه الظروف إلى على دربى ويتفقون فيما بينهم على أحكام إرجاع الشيء المعني بالعملية.</p>

            <h2 class="sub2">٨-٤-٢</h2>
            <p>يتفق الأطراف على أن على دربى ما هي إلا تطبيق للتواصل لا تقدم أية خدمات نقل ولا تتصرف بصفتها شاحناً أو ناقلاً أو مستلماً، وبالتالي فإن مسؤوليتها يمكن أن تترتب فقط عن تشغيل وتوفير التطبيق الخاص بها.</p>

            <h2>٩- الضوابط:</h2>
            <p>
                يوافق جميع الأعضاء عند استخدام البرنامج على أن يكونوا على اطلاع كامل وملائم بشأن كافة الالتزامات والضوابط القانونية المطبقة في كل دولة (في الدولة التي ترسل منها الشحنة وفي وجهة الاستلام، شريطة أن يكون المكانين في بلدين مختلفين). وبهذا المعنى، يكون على الأطراف الالتزام على سبيل المثال لا الحصر بكافة الالتزامات والضوابط الفنية والقانونية التي تفرضها الدول أو وسائل النقل.
                <br>
                وبصفة على دربى منصة تواصل فقط فإنها ليست مخولة بالوصول بأي حال من الأحوال إلى الشحنة. ولا تعتبر على دربى تحت أي ظرف من الظروف مالكة للشحنة أو محتوياتها. ولا يمكن لعلى دربى أن تتحكم بمحتوى الشحنة وحالة قانونية وملاءمة أية شحنة أيضاً. كما أن على دربى ليست مسؤولة وتخلي مسؤوليتها كذلك بما يتصل بجميع الشحنات أو أي منها. ويكون قبول أي طلب أو إجراؤه وفقاً لمسؤولية العضو. وما لم يرد نص صريح بخلاف ذلك في منصة على دربى، تكون مسؤوليات على دربى مقتصرة على العمل السليم للتطبيق وخدمته بما يحقق مصلحة الأطراف المعنية.
            </p>

            <h2>١٠- عدم تسليم الشحنة أو الشحنة المعيبة:</h2>

            <h2 class="sub">١٠-١ عدم تسليم الشحنة:</h2>
            <p>حسبما وردت الملاحظة بشأنه أعلاه، فإن المسافر ينفذ تسليم الشيء ببذل الجهد المطلوب. وفي حال عدم تسليم الشيء في الزمان والمكان المتفق عليهما مع المرسل بسبب عدم بذل مثل هذا الجهد، فإن المسؤولية التي تترتب عن عدم التسليم، وكقاعدة عامة، تكون على عاتق المسافر.
                <br>
             وإذا حدث عدم التسليم نتيجة لتدخل طرف ثالث (مثل الشرطة)، فإن المسؤولية ستحدد بحسب كل حالة على حدة وذلك بناء على التحقيق الذي تجريه على دربى. وفي جميع الأحوال، فإن على المسافر تقديم دليل رسمي بشأن ما ورد أعلاه.
             <br>
             وإذا لم يتمكن المسافر من إتمام عملية التسليم نتيجة لعدم التزام المستلم (على سبيل المثال عدم تواجد المستلم في المكان المحدد للقاء)، فإن المسافر سيحتفظ بالشيء لمدة 7 أيام. وفي حال لم يطالب المستلم بالشيء خلال الفترة الزمنية المذكورة، فسوف تتحمل على دربى رسوم نقل الشيء إلى نقطة الاستلام عبر البرنامج.
            </p>

            <h2 class="sub">١٠-٢ المنتج المعيب</h2>
            <p>حسبما وردت الملاحظة بشأنه أعلاه، فإن المسافر ينفذ تسليم الشيء ببذل الجهد المطلوب. وفي حال تسليم شيء معيب بسبب عدم بذل مثل هذا الجهد، فإن المسؤولية التي تترتب عن تسليم شيء معيب إلى المستلم تكون على عاتق المسافر.
                <br>
            وإذا حدث الضرر بسبب طرف ثالث (مثل الشرطة)، فإن على المسافر تقديم بلاغ بالواقعة لشركة الخطوط الجوية والمطالبة بالتعويض.
            </p>

            <h2>١١- السداد</h2>

            <h2 class="sub">١١-١ رسوم الخدمة:</h2>
            <p>يجب عليك الموافقة على الشروط التالية قبل اعتماد تسجيلك كسائق : \n- إذا كان سعر الرحلة أقل من 500 ريال يتم احتساب 15% كعمولة التطبيق. \n- إذا كان سعر الرحلة أقل من 1000 ريال يتم احتساب 50 ريال كعمولة التطبيق. \n- إذا كان سعر الرحلة أكثر من 1000 ريال يتم احتساب 100 ريال كعمولة التطبيق.</p>

            <h2 class="sub">١١-٢ الإلغاء:</h2>
            <p>في حال قيام الأطراف بإلغاء الطلب، فعليهم الاتفاق على الشروط الخاصة بإعادة الشيء وإشعار على دربى بإلغاء الخدمة بحيث يمكن المضي قدماً في إعادة الأموال المحتجزة.
                <br>
                ويتفق الأطراف على أن على دربى هو تطبيق تواصل لا تقدم أية خدمات نقل ولا تتصرف بصفتها ناقلاً أو شاحناً أو مستلماً، وبالتالي فإن مسؤوليتها تكمن في تشغيل وتوفير البرنامج الخاص بها لهذا الغرض.
            </p>

            <h2>١٢- حق الانسحاب:</h2>
            <p>وفقاً للنظام المعمول به يكون لديكم الحق في الانسحاب. ولكن هذا الحق لن يكون مستمراً. وعليه فإنكم تقرون بأنه لن يكون لديكم ذلك الحق من لحظة موافقة المرسل والمسافر على تنفيذ عملية التسليم، إلى أن يكون هدف التطبيق المتمثل في ربط الأشخاص ببعضهم البعض قد تحقق بالكامل.</p>

            <h2>١٣- مسؤولية تصفح واستخدام البرنامج:</h2>
            <p>
                يكون الأعضاء ملزمين باستخدام الخدمات والمعلومات والمواد الموجودة على البرنامج وفقاً للقوانين المعنية ووفقاً لهذه الشروط والأحكام العامة. ولا يجوز بأي حال من الأحوال أن ينتهك استخدامكم لهذا البرنامج القوانين والأخلاق السائدة والعادات المتفق عليها أو النظام العام، ويجب عليكم في جميع الأوقات القيام باستخدام ملائم وقانوني للخدمات والمعلومات والمواد الموجودة على البرنامج.
                <br>
                ويجوز لكم بصفتكم مستخدمين ما يلي:
            </p>
            <ul>
                <li>الدخول بشكل مجاني ودون الحصول على تفويض مسبق لتصفح المواد والخدمات الموجودة على البرنامج؛</li>
                <li>استغلال الخدمات والمواد الموجودة في البرنامج بشكل حصري لأغراض الاستخدام الخاص.</li>
            </ul>
            <p>
                ولا يجوز بأي حال من الأحوال الانخراط في الأنشطة التالية:
            </p>
            <ul>
                <li>نشر محتوى أو مادة دعائية ذات طابع عِرقي أو تتضمن كراهية للاجانب، أو تتضمن مادة إباحية، أو غير لائقة، أو ذات طابع تحقيري؛ أو مادة تثير وتشجع على ارتكاب الأعمال الجنائية والعنف وتشويه السمعة أو الحط من قدر الآخرين لأسباب تتعلق بالعُمر، أو نوع الجنس، أو الدين، أو المعتقدات؛ أو تحض على ارتكاب أو تشجيع أو الإثارة، سواء كان ذلك بشكل مباشر أو غير مباشر، لأعمال إرهابية؛ أو التي تنافي حقوق الإنسان والحقوق الأساسية والحريات للجهات الأخرى، أو التشريعات المعمول بها، أو الأخلاق، أو العادات المتفق عليها أو النظام العام؛ أو لأغراض الأذى التي يمكن أن تلحق الضرر أو تعيق أي دخول إلى هذه المواد بما يلحق الأذى بعمل على دربى أو الجهات الأخرى.</li>
                <li>القيام بأعمال تنتهك حقوق الملكية الفكرية للمالكين الشرعيين لهذه الحقوق.</li>
                <li>التسبب بالضرر لنظم الحاسب الآلي لعلى دربى، ومزوديها أو جهات أخرى، و/أو إدخال أو نشر فيروسات الحاسب الآلي، أو رموز أو برمجيات ضارة أو أنواع أخرى من النُظُم التي قد تؤدي إلى أي مما يلي: إلحاق الضرر أو التغيير على أنظمة الحاسب الآلي؛ أو التغيير غير المصرح به للمحتوى أو البرامج أو النُظم التي يمكن الدخول إليها عن طريق المواد أو الخدمات الموجودة على البرنامج، أو على نظم المعلومات، أو الملفات أو معدات الحاسب الآلي الخاصة بالمستخدمين؛ أو الدخول غير المصرح به لأية مواد وخدمات على البرنامج.</li>
                <li>نشر مواد إعلانية بأية وسيلة كانت، ويكون ذلك على وجه التحديد عبر إرسال رسائل إلكترونية، عندما لا يكون نشر الإعلان مطلوباً أو مصرحاً به من قبل المستلم.</li>
                <li>استغلال البرنامج أو الخدمة، سواء بشكل كلي أو جزئي، لترويج أو بيع أو التعاقد أو نشر إعلانات الخاصة أو تلك التي تعود إلى جهات أخرى دون الحصول على الموافقة الخطية المسبقة من على دربى، أو التي تتضمن الوصلات الكثيفة التشعب (hyperlinks) التي توجد على المواقع الإلكترونية الخاصة أو التجارية على التطبيق باستثناء ما يكون مصرحاً به بشكل صريح من على دربى.</li>
                <li>استغلال الخدمة والمواد التي تقدم عبر التطبيق بطريقة تتعارض مع الشروط والأحكام العامة التي تنظم استخدام خدمة و/أو محتوى محدد يمكن أن يلحق الضرر أو يقلل من حقوق المستخدمين الآخرين.</li>
                <li>القيام بالتخلص أو التعديل بأية طريقة كانت من الحماية و/أو آليات التعريف الخاصة بعلى دربى أو مالكيها الشرعيين، التي قد تكون موجودة على التطبيق ، أو رموزها أو شعاراتها أو علاماتها التجارية التي قد تقوم على دربى أو حاملي الحقوق الشرعيين الآخرين بإضافتها على المواد التي ينشئونها والتي قد تكون مشمولة بحقوق الملكية الفكرية.</li>
                <li>القيام بإدخال علامات وصفية (metatags) على صفحات الشبكة العنكبوتية و/أو التطبيقات التي تكون تحت مسؤوليتكم أو ملكيتكم أو مسؤولية أو ملكية جهات أخرى، دون الحصول على تصريح خطي مسبق من على دربى، حيث يكون هذا الإدخال غير مصرح به لهذه العلامات الوصفية مطابقاً للعلامات التجارية أو الشعارات أو الأسماء التجارية لعلى دربى؛ أو القيام باستغلال العلامات التجارية أو الشعارات أو الأسماء التجارية أو أي نوع آخر من علامات التعريف الخاضعة لحقوق الملكية الفكرية دون الحصول على التصريح الخطي المسبق والصريح من المالك الشرعي لها.</li>
                <li>القيام بإدخال رابط كثيف التشعب على صفحات الشبكة العنكبوتية و/أو التطبيقات التي تكون تحت مسؤوليتكم أو ملكيتكم، حيث يؤدي هذا الرابط إلى إنتاج نافذة أو جلسة تصفح برمجيات يستخدمها المستفيدون من موقعكم الذي يتضمن علامات تجارية، أو أسماء تجارية أو علامات مميزة تملكونها والتي يجرى عرضها على التطبيق.</li>
            </ul>
            <p>
                يكون العضو مسؤولاً أمام على دربى أو الجهات الأخرى فيما يتعلق بأية أضرار مهما كان نوعها والتي يمكن أن تكون قد نشأت نتيجة عدم القدرة على الانصياع أو الالتزام بشكل مباشر أو غير مباشر بهذه الشروط. ويكون على على دربى أن تتصرف في جمع الأوقات لضمان احترام الإطار القانوني المطبق، ويمكنها أن تمنع بشكل جزئي أو كلي في أي وقت من الأوقات ودون الحاجة إلى إشعار مسبق دخول أي مستخدم إلى البرنامج في حال ظهور واحد أو أكثر من الحالات الواردة في هذا البند.
                <br>
                على الرغم من أن على دربى تقوم بخطوات معقولة لمنع دخول الفيروسات وغيرها من الرموز الالكترونية الأخرى على التطبيق فإن على دربى لا تصرح أو تضمن بأن الخدمة خالية من هذه العوامل المدمرة. وهى ليست مسئولة عن أي أضرار أو أذى يعزى إلى هذه العوامل أو ناشئة مباشرة أو غير مباشرة عن هذه العوامل.
            </p>

