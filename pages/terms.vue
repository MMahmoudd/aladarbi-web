            <h2>١٤- نظام التحقق:</h2>
            <p>يرجى ملاحظة أنه لزيادة الثقة، وتجنب الأخطاء الطباعية والمعلومات الخاطئة، فإنه يجوز لعلى دربى في أي وقت من الأوقات إجراء عملية ضبط وتحقق من المعلومات التي يقدمها الأعضاء (على وجه التحديد المعلومات الشخصية ومعلومات السفر). ويقر الأفراد بأن يقدموا معلومات حقيقية ودقيقة، بحيث لا تكون تحت أي ظرف من الظروف معلومات مزورة أو قديمة أو غير مكتملة عن أنفسهم أو عن السفر أو الشحنة و/أو أية معلومات أخرى ملائمة لأداء الخدمات. وللقيام بذلك، ستطلب على دربى من الأعضاء تحميل نسخة من وثيقة هوياتهم (مثل بطاقة الهوية الوطنية أو جواز السفر).
                <br>
                يرجى العلم أن على دربى تتمتع بحق منع أو رفض و/أو إلغاء حساب أي عضو قد ينتهك اشتراطات المعلومات الواردة أعلاه.
            </p>


            <h2>١٥- المراجعات:</h2>
            <p>تقوم على دربى بتوفير نظام يمكن للأعضاء من خلاله الحصول على التأهيل ليكونوا أعضاء فيه، كما تقوم بمراجعة العمل على خدمة على دربى الذي يقوم به أي عضو. وبهذا المعنى، فإن على دربى تشجع المستخدمين على التأهيل لخدمة على دربى بشكل عام، ونشر أية ملاحظة و/أو رأي بشأن ما يلي:</p>
            <ul>
                <li>المسافر(في حال كنت شاحناً) أو بشأن المرسل(في حال كنت ناقلاً)؛</li>
                <li>المسافر(في حال كنت مستلماً) أو بشأن المستلم (في حال كنت ناقلاً).</li>
            </ul>
            <p>لذا فإن أراءكم، إضافة إلى الآراء التي تنشر من قبل عضو آخر بشأنكم، ستنشر على صفحة حسابكم الشخصي بحيث يمكن مشاهدتها وتؤخذ بالحسبان من كافة الأعضاء. لذا يرجى العلم أن لعلى دربى الحق في إزالة أية ملاحظة أو رأي قد يعتبر غير مناسب أو غير قانوني أو يمكن أن ينتهك هذه الشروط والأحكام العامة. ويجوز للتطبيق أن يتضمن معلومات أو محتويات تقدمها مصادر خارج على دربى، ويشمل ذلك المعلومات التي يقدمها المستخدمون. ولا تضمن على دربى ولا تقبل أية مسؤولية عن حقيقة أو نزاهة أو دقة أي من هذه المعلومات و/أو المحتويات.</p>

            <h2>١٦- الخصوصية:</h2>
            <p>يوافق العضو على أنه يمنح ويفوض ويوافق على قيام على دربى باستخدام جميع البيانات والصور والتعليقات التي يتم نشرها على التطبيق سواء كانت منشورة من قبله أو من قبل عضو آخر وكانت متصلة بحساب العضو. كما يوافق على حق على دربى في مشاركة هذه البيانات والصور والتعليقات مع أي أطراف أخرى.</p>

            <h2>١٧- متفرقات:</h2>

            <h2 class="sub">١٧-١ الملكية الفكرية والصناعية:</h2>
            <p>
                تكون كافة المواد والمعلومات الموجودة على البرنامج خاضعة للقانون الحالي الخاص بالملكية الفكرية و/أو الصناعية. وتعود الحقوق في المواد والعناصر الأخرى التي تظهر على البرنامج (ويشمل ذلك على سبيل المثال لا الحصر الرسومات والنصوص والأشكال والصور والمواد المسموعة والمرئية والبرمجيات والرموز التعريفية المميزة، وما إلى ذلك) إلى على دربى، أو أنها في بعض الحالات تعود إلى جهات أخرى كانت قد رخصت أو حولت ما وردت الإشارة إليه هنا إلى على دربى. كما تملك على دربى الحقوق في شعاراتها وأسمائها التجارية ونطاقاتها وعلاماتها التجارية.
                <br>
                ولا يجوز بأي حال من الأحوال أن يُفهم أن الدخول إلى البرنامج أو استخدامها أو البحث عن مواد فيها أو تنزيلها و/أو استخدام خدمات على دربى من قبل الأعضاء يعني أن على دربى أو مالك الحقوق، بحسب مقتضيات الحال، يعني التنازل أو النقل أو الترخيص، سواء كان ذلك بشكل جزئي أو كلي، للحقوق التي وردت الإشارة إليها أعلاه.
                <br>
                وبناء على ذلك، فإنه لا يجوز للأعضاء حذف أو منع أو التلاعب بالإشعارات الخاصة بحقوق المؤلف (ويُشار إليها بـ "حقوق الطبع") أو أية بيانات أخرى تدخل في الخدمة التي تحدد حقوق على دربى أو حقوق الآخرين، ولا حذف أو منع أو التلاعب بأجهزة الحماية الفنية أو أية معلومات و/أو وسائل التعريف التي تدخل ضمنها.
                <br>
                وعلى وجه التحديد، يحظر وبشكل صارم القيام بعملية تضمين جزئي أو كلي لأية مادة أو عناصر في البرنامج بحيث تكون هذه المواد أو العناصر من خارج الموقع دون الحصول على تصريح خطي مسبق من مالك الموقع.
                <br>
                تعتبر الإشارة إلى الأسماء التجارية، والعلامات التجارية، والعلامات التجارية المسجلة، والشعارات أو أية علامات تعريفة مميزة أخرى، سواء كانت ملكاً لعلى دربى أو شركات أخرى، على أنها توحي بأن ما وردت الإشارة إليه هنا لا يجوز أن يُستخدم دون موافقة على دربى أو مالكيها الشرعيين. ولا يعطي الدخول أو الاستخدام للتطبيق في أي وقت من الأوقات، ما لم يتم التعبير عن ذلك بشكل صريح، لا يعطي الأعضاء الحق في العلامات التجارية، أو الشعارات و/أو العلامات التعريفية المميزة الواردة هنا أو التي تكون محمية بالقانون.
                <br>
                وتعتبر كافة حقوق الملكية الفكرية والصناعية محفوظة، وعلى وجه التحديد، فإنه لا يجوز تعديل أو إرفاق أو نسخ أو إعادة استخدام أو استغلال بأي شكل من الأشكال أو إعادة إنتاج أو تحويل أو استنساخ أو ترجمة أو التنازل أو بيع أو تأجير أو إعارة أو البث على الملأ أو إعادة النشر مرة أخرى أو غيرها، أو تُضَمَّنُ في الملفات، أو إرسال بالبريد العادي، أو البث، أو الاستخدام، أو المعالجة أو التوزيع بأية طريقة من أجل الاستخدام العام أو التجاري لأية محتويات وعناصر ومنتجات, دون إذن كتابي صريح من على دربى أو مالك الحقوق ، حسب الحالة.
                <br>
                إذا أدى أي تصرف أو تقصير، سواء كان متعمداً أو نتيجة للإهمال، مما ينسب بشكل مباشر أو غير مباشر إلى عضو مسجل في الخدمة، إلى انتهاك حقوق الملكية الفكرية والصناعية لعلى دربى أو تلك الحقوق العائدة لطرف آخر، وأدى ذلك بعلى دربى إلى أن تتعرض للضرر أو الخسارة أو الدخول في مسؤوليات مشتركة، أو تحمل مصاريف مهما كان نوعها، أو فرض عقوبات عليها، أو اتخاذ إجراءات قسرية ضدها، أو دفعها غرامات، أو دفع مبالغ مالية، نتيجة نشوء أية مطالبة أو طلب أو إجراء أو قضية أو عملية قانونية، سواء كانت مدنية أو عقابية أو إدارية، فإنه يكون لعلى دربى الحق في التصرف ضد العضو المذكور باستخدام كافة الوسائل القانونية المتاحة لها والمطالبة بأية مبالغ تتساوى مع التعويضات أو الضرر المعنوي أو الضرر الذي لحق بصورتها أو الضرر المترتب على أفعال أخرى وكذلك الأرباح التي خسرتها أو تكاليف الدعاية أو أي نوع آخر من التكلفة الذي قد يكون لازماً لإصلاح الضرر، أو أية مبالغ تتساوى مع العقوبات أو الإدانات أو الفوائد التي تترتب عن التأخر في السداد، أو تكاليف الدفع مقابل العقوبات المالية التي تعرضت لها على دربى، أو التكاليف القانونية أو الدفاعية (يشمل ذلك تكاليف المحامين) في أي إجراء قانوني كانت على دربى قد خضعت فيه للمحاكمة للأسباب المذكورة أعلاه عن الأضرار والتداعيات التي نشأت عن الضرر الذي تسبب به التصرف أو التقصير في التصرف، دون ضرر بحق ممارسة أية أفعال أخرى يكون لعلى دربى فيها حق قانوني.
                <br>
                وعلاوة على ما ورد أعلاه، يتعهد الأعضاء بأن الشيء المراد إرساله عبر المسافر سيحترم القانون الحالي المعمول به بشأن حقوق الملكية الفكرية و/أو الصناعية.
            </p>            
            
            <h2 class="sub">١٧-٢ الصور التي تُحَمَّلُ على البرنامج:</h2>
            <p>
                لا يجوز للأعضاء استخدام أو نشر صور تنتهك حقوق أطراف ثالثة أخرى أو أي قانون مطبق، ويشمل ذلك أي محتوى غير قانوني أو بذيء أو إباحي أو مسيء أو مشوه للسمعة أو مضلل أو ذي محتوى عرقي أو لا أخلاقي أو يمس النظام العام، أو يشجع على ارتكاب أنشطة غير قانونية أو إباحية أو مسيئة أو مشوهة للسمعة أو مضللة أو ذات محتوى عرقي أو كراهية الأجانب، أو تلك التي تؤدي إلى تمييز ضد أي شخص، كما لن تقبل أية إشارات إلى أي مستوى من اللغة البذيئة.
                <br>
                ويتعهد الأعضاء بشكل صريح بملكية وأصالة أية صور تُحَمَّلُ على التطبيق و/أو الموقع. وبهذا المعنى، يقر الأعضاء بأنهم لن يستخدموا أي محتوى ليس لديهم أي حق قانوني في إعادة إنتاجه أو توزيعه أو تعديله أو نسخه أو إرفاقه أو وضعه في متناول جهات أخرى. ويعتبر أنه قد جرى التنازل عن كافة الصور التي تُحَمَّلُ على التطبيق و/أو الموقع لصالح على دربى دون مقابل، وذلك ضمن حدود الحد الأعلى للوقت المسموح به وفي كافة أرجاء العالم، ويجوز استخدامها ضمن حدود على دربى التي حددتها الأنظمة المطبقة، دون وجود التزامات بشأن سرية المحتويات أو المعلومات المذكورة.
                <br>
                ونظراً إلى الكم الهائل من المعلومات التي يمكن تخزينها على التطبيق فإنه يستحيل على على دربى التحقق من أصالة المحتويات التي يقدمها المستخدم أو التحقق من عدم انتهاك حقوق الأطراف الأخرى؛ ويكون المستخدم وحده مسؤولاً عن جميع الآثار المترتبة عن أية مخالفات قد تنتج عن تقديمه للمعلومات.
                <br>
                ويجوز لعلى دربى أن تعدل المعلومات التي يقدمها المستخدمون لتكييفها بحسب الاشتراطات التي يفرضها على دربى و/أو موقعها على صيغة المعلومات.
            </p>

            <h2 class="sub">١٧-٣ حقوق الصور:</h2>
            <p>كما يمنح الأعضاء تفويضهم بالتحديد لمعالجة بياناتهم الشخصية ونقل واستخدام حقوق صورهم وفقاً للأحكام المنصوص عليها في هذه الشروط والأحكام العامة.</p>

            <h2 class="sub">١٧-٤ الوصلات والوصلات الكثيفة التشعب للموقع:</h2>
            <p>إذا قام أي عضو بإدخال وصلات أو وصلات كثيفة التشعب خاصة به إلى التطبيق فعليه أن يلتزم بالشروط الموضحة أدناه؛ ولا يعفي الجهل بهذه الشروط الأعضاء من أية مسؤوليات تترتب عن عدم الالتزام هذه الشروط:</p>
            <ul>
                <li>يُمنع، وفقاً للتشريعات المعمول بها حاليا القيام بإنشاء "إطارات" أو مربعات من أي نوع تسمح بمشاهدة التطبيق كلياً أو جزئياً عن طريق عناوين إنترنت مختلفة عن تلك الموجودة في التطبيق وعلى أي حال، عندما يجري السماح بمشاهدة عناصر التطبيق مع محتويات أخرى، فلا يكون أي جزء من البرنامج و/ أو المحتويات، يمكن أن:
                    <ul>
                        <li>(1) يؤدي إلى أو قد يؤدي إلى حدوث خطأ أو إرباك أو خداع لدى الأعضاء فيما يتعلق بالمصدر الحقيقي للعناصر المعروضة أو الخدمات المستخدمة؛</li>
                        <li>(2) يشكل عملاً يتسم بالمقارنة غير العادلة أو التقليد.</li>
                        <li>(3) يسعى إلى استغلال العلامة التجارية والمكانة الخاصة لعلى دربى؛ أو</li>
                        <li>(4) يكون محظوراً بحسب التشريعات الحالية.</li>
                    </ul>
                </li>
                <li>لا تتضمن الصفحة التي تحتوي على الرابط أي نوع من البيانات أو المؤشرات الزائفة أو غير الدقيقة أو غير الصحيحة بخصوص على دربى أو موظفيها أو عملائها أو جودة الخدمات التي تقدمها.</li>
                <li>لا يجوز بأي حال من الأحوال الكتابة أو السماح بالفهم في الصفحة التي تحتوي على الوصلة بأن على دربى كانت قد منحت موافقتها على تضمين الوصلة فيها أو أنها بأية طريقة يقوم بتمويل أوتتعاون مع أو تتحقق أو تشرف على خدمات الأعضاء.</li>
                <li>يحظر استخدام أية علامات تجارية لعلى دربى، سواء كانت اسمية، أو مؤلفة من صور أو من الاثنين معاً، أو من أية علامة تعريفية مميزة، باستثناء تلك الحالات التي يسمح بها القانون أو التي يتم التصريح بها صراحة من على دربى، والتي تنص دائماً على أن الاتصال المباشر بالبرنامج مسموح وفقاً للشروط المنصوص عليها في هذه الفقرة.</li>
                <li>
                    يجب أن تلتزم الصفحة التي تتضمن الوصلة أو الوصلة الكثيفة التشعب وبدقة بالقانون ولا يجوز بأي حال من الأحوال القيام بالتقديم أو الاتصال بمحتوى يعود للأعضاء أو أطراف أخرى بما يتصف:
                    <ul>
                        <li>(1) بأنه غير قانوني، أو ضار، أو مخالف للاخلاق والعادات الحسنة (ويشمل ذلك على سبيل المثال لا الحصر المواد الإباحية والعنف والتفرقة العنصرية، وما إلى ذلك).</li>
                        <li>(2) يشجع الأفراد على أن يعتقدوا خطأ بأن على دربى تؤدي أو توصي أو تتمسك أو بأي شكل من الأشكال وتدعم الأفكار والتصريحات أو العبارات في الموقع، سواء كانت قانونية أو غير قانونية؛</li>
                        <li>(3) تكون غير ملائمة أو ليست مناسبة لأنشطة على دربى فيما يتعلق بالموقع أو المحتويات أو الهدف من وراء الصفحة.</li>
                    </ul>
                </li>
                <li>لا يعني التصريح بإدخال وصلة أو وصلة كثيفة التشعب بأي حال من الأحول الموافقة على إعادة إنتاج الجوانب المرئية والوظيفية الخاصة بالبرنامج. وعلى وجه التحديد، فإن التصريح بإدخال وصلات كثيفة التشعب إلى الموقع و/أو المحتويات يكون خاضعاً لاحترام كرامة الإنسان وحريته. ولا يجوز للموقع الإلكتروني الذي يحتوي على الوصلة أن يحتوي على معلومات أو محتويات غير قانونية، أو تخالف الأخلاق أو العادات الحسنة أو النظام العام، ولا يجوز كذلك أن يحتوي على محتويات تنتهك حقوق الأطراف الأخرى.</li>
                <li>لا يعني إنشاء وصلة كثيفة التشعب بأي شكل من الأشكال وجود علاقة بين على دربى ومالك الصفحة التي تحتوي على الوصلة ولا قبول على دربى أو موافقتها على المحتويات أو الخدمات التي تقدم فيها إلى الجمهور.</li>
            </ul>
            <p>ويجوز لعلى دربى أن تطلب، في أي وقت من الأوقات ودون التزام بتبرير هذا الطلب، بأن أية وصلة أو وصلة كثيفة التشعب في البرنامج يجب أن تزال وأن يقوم الشخص المسؤول عن الخدمة التي تحتوي على الوصلة بالتخلص منها فوراً.</p>

            <h2 class="sub">١٧-٥ عدم وجود اتفاقيات أخرى:</h2>
            <p>تشكل الأحكام الواردة في هذه الشروط مجمل الاتفاقية بينكم وبين على دربى بشأن استخدام البرنامج. ويحق لتطبيق على دربى تغيير هذه الشروط والأحكام العامة في أي وقت. و يتم اعتبار أي من هذه التغييرات سارية المفعول في اليوم الذي يتم نشرها فيه على البرنامج (أنظر التاريخ في أعلى الصفحة). لذا يجب عليك قراءة الشروط والأحكام العامة في كل مرة تستخدم فيها البرنامج و بدخولك للمنصة وتسجيل حساب أو استخدام الخدمات التي تقدمها البرنامج، فإنك توافق على الالتزام بالشروط والأحكام العامة. في حالة عدم موافقتك على كل الشروط والأحكام العامة فيجب عليك التوقف عن استخدام البرنامج
            <br>
            وفي حال اعتبر أي من هذه الأحكام غير صالح أو غير قابل للتطبيق، فيجوز أن يحل محله حكم آخر يكون على قدر كبير من التشابه مع الحكم السابق. ولكن يجب ألا يؤثر هذا أو يلحق الضرر ببقية الأحكام، التي يجب أن تبقى دون تأثر بفعل أية فقرة أو حكم يكون غير قانوني أو غير صالح أو غير قابل للتنفيذ، ويجب أن تبقى نافذة بالكامل.
            </p>

            <h2 class="sub">١٧-٦ القانون الذي يحكم الاتفاقية والسلطة القضائية:</h2>
            <p>تكون هذه الشروط والأحكام العامة خاضعة للقانون الساري في دولة المرسل وقت طلب الخدمة.</p>

            <h2 class="sub">١٧-٧ إلغاء تسجيل العضو:</h2>
            <p>يجوز للمستخدم إلغاء تسجيله عن طريق التواصل مع على دربى رسمياً بخصوص ذلك. ويحق لعلى دربى التحقيق وبذل الجهد المناسب لتأكد من عدم وجود أي إلتزامات أو تلاعب من قبل العضو قبل إتمام عملية إلغاء التسجيل.</p>

            <h2>سياسة على دربى اثناء الرحلة</h2>
            <p>نحن في على دريى نهتم لكباتننا وعملائنا وزملائنا على حد سواء، والجميع طبعاً يقدر عمل الكباتن وتفانيهم، فهم الشركاء الذين نثق بهم لتوصيلنا من مكان إلى آخر أو لتوصيل أطفالنا أو حتى لتوصيل أغراضنا، وذلك في مئات المدن. الرجاء الانتباه للنقاط  التالية لنحافظ معاً على هذا المستوى من الخدمة والثقة. </p>

            <h2 class="sub">كن كريماً مع الكابتن</h2>
            <ul>
                <li>ألقِ عليه التحية وابتسم وتصرف معه باحترام في جميع الأوقات.</li>
                <li>يرجى التعاون مع الكابتن وتسليم الطرد او الشحن المراد توصيله</li>
            </ul>

            <h2 class="sub">ادفع تكلفة مشوارك </h2>
            <ul>
                <li>للدفع نقداً: حاول أن تبقي معك مبلغاً كافٍ، أو يمكن إختيار الدفع عند التسليم.</li>
                <li>التسديد المتأخر أو عدم تسديد المبلغ قد يتسبب للأسف بحظر حسابك في على دربى.</li>
            </ul>

            <h2 class="sub">استخدم الجي بي اس لتحديد نقاط الانطلاق أو الوصول</h2>
            <ul>
                <li>استخدم نظام الجي بي اس دائماً وساعد الكابتن بإرشاده شفهياً إذا كان العنوان معقداً.</li>
                <li>أعلم الكابتن في حال كنت تفضل طريقاً معيناً.</li>
            </ul>
            
            <h2 class="sub">كن جاهزاً عند وصول الكابتن وحاول أن تتجنب إلغاء المشاوير دون سبب</h2>
            <ul>
                <li>إيجاد مكان للوقوف قد يكون من الصعب إيجاده أحياناً، لذلك حاول أن لا تجعل الكابتن ينتظر كثيراً.</li>
                <li>كن بانتظار الكابتن بمجرد وصوله إلى مكان الإنطلاق كي تتجنب رسوم الانتظار أو الإلغاء. يحق للكابتن إلغاء المشوار في حال تجاوزت فترة انتظاره لك بين ٥ إلى ١٠ دقائق. </li>
            </ul>

            <h2 class="sub">اشكر الكابتن على خدمته المميزة</h2>
            <ul>
